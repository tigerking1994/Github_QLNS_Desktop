CREATE PROCEDURE [dbo].[sp_rpt_qtc_nqlkp_phucluc_bh]
	@listTenDonVi ntext,
	@namLamViec int,
	@dvt int
AS
BEGIN
		-- Get chi tiet
			select 
					SUM(fTien_ThucChi) fTien_ThucChi,
					iID_MaDonVi
					into #tempChitiet
			from BH_QTC_Nam_KinhPhiQuanLy_ChiTiet
			where iID_QTC_Nam_KinhPhiQuanLy in
			(
				select ID_QTC_Nam_KinhPhiQuanLy from BH_QTC_Nam_KinhPhiQuanLy
				where iNamLamViec=@namLamViec
				and iID_MaDonVi in  (SELECT * FROM f_split(@listTenDonVi))
			)

			and sXauNoiMa like '9010003%'
			group by iID_MaDonVi

			-- Get du toan
			SELECT 
			  SUM(cast(fTienTuChi as float)) AS fTienDuToan,
			  iID_MaDonVi
			  --sXauNoiMa
			 into #tempdutoan
		   FROM BH_DTC_PhanBoDuToanChi_ChiTiet
		   WHERE iID_DTC_PhanBoDuToanChi IN
			   (SELECT ID
				FROM BH_DTC_PhanBoDuToanChi
				WHERE sSoQuyetDinh <> ''
				  AND sSoQuyetDinh IS NOT NULL
				  AND iNamLamViec = @namLamViec
				  AND bIsKhoa=1)
			AND iID_MaDonVi in  (SELECT * FROM f_split(@listTenDonVi))-- donvi
			AND  sXauNoiMa like '9010003%'
		   GROUP BY iID_MaDonVi

		   -- Get tham dinh
		   SELECT fSoThamDinh, iID_MaDonVi into #tempThamDinh
						FROM BH_ThamDinhQuyetToan_ChungTuChiTiet 
						WHERE iNamLamViec = @namLamViec - 1 and iMa = 254 and iID_MaDonVi in (SELECT * FROM f_split(@listTenDonVi))


		   -- Get data
		   select 
				 CAST( ROW_NUMBER() OVER(ORDER By dv.iID_MaDonVi) AS int) as STT,
				  ISNULL(tdinh.fSoThamDinh,0) / @dvt  as FTienDaThucHienNamTruoc,
				  isnull(ctct.fTien_ThucChi,0) / @dvt as FTienQuyetToan,
				  (ISNULL(tdinh.fSoThamDinh,0) / @dvt + ISNULL(dtoan.fTienDuToan,0) ) / @dvt as FTienCong,
				  ISNULL(dtoan.fTienDuToan,0) / @dvt as FTienNamNay,
				  Case when  ISNULL( ctct.fTien_ThucChi,0) -  (ISNULL(tdinh.fSoThamDinh,0) + ISNULL(dtoan.fTienDuToan,0)) >0  then (ISNULL( ctct.fTien_ThucChi,0) -  (ISNULL(tdinh.fSoThamDinh,0) + ISNULL(dtoan.fTienDuToan,0) )) / @dvt else 0 end FTienThua,
				  Case when  (ISNULL(tdinh.fSoThamDinh,0) + ISNULL(dtoan.fTienDuToan,0) )-ISNULL( ctct.fTien_ThucChi,0)  >0  then  ((ISNULL(tdinh.fSoThamDinh,0) + ISNULL(dtoan.fTienDuToan,0) )-ISNULL( ctct.fTien_ThucChi,0)) / @dvt  else 0 end FTienThieu,
				  dv.sTenDonVi as sTenDonVi,
				  dv.iID_MaDonVi as  idDonVi
				from #tempChitiet ctct
		   left join #tempdutoan dtoan on ctct.iID_MaDonVi=dtoan.iID_MaDonVi
		   left join DonVi dv on ctct.iID_MaDonVi=dv.iID_MaDonVi
		   left join #tempThamDinh tdinh on ctct.iID_MaDonVi=tdinh.iID_MaDonVi
		   where dv.iNamLamViec=@namLamViec
		   and dv.iTrangThai=1

END
;
;
;
