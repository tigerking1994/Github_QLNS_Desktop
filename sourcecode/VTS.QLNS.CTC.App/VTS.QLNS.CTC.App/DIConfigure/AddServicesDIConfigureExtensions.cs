        public static IServiceCollection AddSingletonFromNamespace(this IServiceCollection services, string name, string namespaceName)
        {
            //string name = "VTS.QLNS.CTC.App";
            //string namespaceName = $"{name}.Service";
            var assemblies = AppDomain.CurrentDomain.GetAssemblies().Where(x => x.FullName.StartsWith(name)).ToArray();
            foreach (var assembly in assemblies)
            {
                var types = assembly.GetTypes().Where(x => x.Namespace != null
                    && (x.IsClass || x.IsInterface)
                    && x.Namespace.StartsWith(namespaceName, System.StringComparison.InvariantCultureIgnoreCase));
                foreach (var type in types)
                {
                    if (services.All(x => x.ServiceType != type))
                    {
                        if (type.IsInterface)
                        {
                            foreach (var x in types.Where(x => type.IsAssignableFrom(x) && !x.IsInterface))
                            {
                                services.AddSingleton(type, x);
                            }
                        }
                    }
                }
                foreach (var type in types)
                {
                    if (services.All(x => x.ServiceType != type))
                    {
                        if (type.IsPublic && !type.IsAbstract)
                        {
                            services.AddSingleton(type, type);
                        }
                    }
                }
            }
            return services;
        }
